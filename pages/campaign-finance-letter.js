import NewLayout from "../components/NewLayout/NewLayout";


function CampaignFinanceLetter() {
  return (
    <NewLayout pageTitle="Publicly Financed Campaigns – Letter">
      <p>
        Dear Elected Officials,
      </p>
      <p Style="text-indent:1cm">
        We write to you today in support of publicly financed campaigns for
	state legislators, and urge you to re-institute a program like those
	described by <a href="https://www.commoncause.org/massachusetts/our-work/reduce-moneys-influence/citizen-funded-elections/">Common Cause</a>.
	The Citizens United decision severely restricted the states’ ability
	to regulate campaign finance, leaving these programs
	as <a href="https://www.brennancenter.org/our-work/analysis-opinion/public-campaign-financing-bright-spot-shadow-citizens-united">
	one of the few mechanisms</a> available to counteract the distortive
	effects of money in politics. Publicly financed campaigns amplify
	the voice of small-donors, incentivize
	a <a href="https://www.demos.org/media/report-details-positive-effects-public-campaign-financing-connecticut-0">more responsive legislature</a> and
	help to <a href="https://publiccorruption.moreland.ny.gov/sites/default/files/moreland_report_final.pdf">rebuild public trust</a>.
      </p>
      <p Style="text-indent:1cm">
        Moreover, Massachusetts voters have unequivocally demonstrated
	support for publicly funded campaigns, including for state
	legislators. <a href="https://ballotpedia.org/Massachusetts_Campaign_Finance_Reform_Initiative,_Question_2_(1998)">In a 1998 ballot measure</a>,
	58.4% of participating voters backed new laws to implement
	public financing of elections and to establish clear spending
	limits for candidates accessing public funds. The Clean
	Elections Law was adopted the same year and began a program
	to fund state legislative campaigns. It only lasted a few 
	years, as the legislature <a href="https://www.nytimes.com/2003/06/21/us/massachusetts-legislature-repeals-clean-elections-law.html">repealed the Law</a> in
	2003 as part of the compromise to pass the state budget. Public
	funding remains available to candidates running for statewide
	office, but notfor those candidates running for the Legislature.
	However, even the current statewide program is <a href="https://www.wbur.org/news/2018/06/09/public-financing-mass-governor">underfunded</a>.
      </p>
      <p Style="text-indent:1cm">
        Under Common Cause’s citizen-funded campaign proposal, candidates
	who meet donor and fundraising benchmarks would receive matching
	public funds for their campaigns. This additional support would
	incentivize closer relationships with constituents, who are best
	positioned to give the required small donations, and would enable
	more Massachusetts residents to run for political office. Candidates
	receiving public funding would be required to participate in at least
	one debate before the primary and at least 2 debates after the primary.
	These debates will allow candidates to share their policy priorities
	and serve to inform voters.
      </p>
      <p Style="text-indent:1cm">
	Research and data from other states indicates a clear correlation 
	between publicly funded campaigns and robust, constituent-focused
	representation. When <a href="https://www.demos.org/sites/default/files/publications/FreshStart_PublicFinancingCT_0.pdf">Connecticut</a> implemented
	publicly financed campaigns, legislators were able to spend less time fundraising and
	spend more time with constituents. Recognizing the increased impact
	of their financial support, citizens educated themselves on policy 
	priorities and started donating to campaigns. The influence of 
	lobbyists declined, more bipartisan bills were passed, and bills 
	passed were more closely aligned with public opinion. Public 
	financing also empowered more people to run for office and shifted
	the demographic representation in the Legislature to more closely
	reflect the make-up of the state. In <a href="https://www.commoncause.org/massachusetts/our-work/reduce-moneys-influence/citizen-funded-elections/">Arizona</a>, after
	public financing was passed in 2000, the state saw a 20% increase in
	the number of voters between the 1996 and 2004 presidential elections.
	The people of Massachusetts deserve to be represented by legislators
	who reflect the people and the policies of the people they serve;
	adopting public financing would help achieve that goal.
      </p>
      <p Style="text-indent:1cm">
	For these reasons, we urge you to consider bills like <a href="https://malegislature.gov/Bills/192/SD122">SD.122</a>, and
	re-establish public funding for the campaigns of state legislators in Massachusetts.
      </p>
      <p Style="text-indent:1cm">
        Signed,
      </p>
      <p Style="text-indent:1cm">
        <i>The Good Governance Project</i>
      </p>
    </NewLayout>
  );
}

